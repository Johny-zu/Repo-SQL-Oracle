CREATE OR REPLACE PROCEDURE HOLA_MUNDO IS
L_MENSAJE VARCHAR(100) := '¡HOLA MUNDO!';
BEGIN
DBMS_OUTPUT.PUT_LINE(L_MENSAJE);
END HOLA_MUNDO;
/

-- Para ejecutar un PROCEDURE es
