-- PROCEDURE 2
CREATE OR REPLACE PROCEDURE HOLA_LUGAR(LUGAR_IN IN VARCHAR2) IS
L_MENSAJE VARCHAR2(100);
BEGIN
L_MENSAJE := '¡HOLA ' || LUGAR_IN;
DBMS_OUTPUT.PUT_LINE(HOLA_LUGAR);
END HOLA_LUGAR;
/

EXECUTE HOLA_LUGAR;

